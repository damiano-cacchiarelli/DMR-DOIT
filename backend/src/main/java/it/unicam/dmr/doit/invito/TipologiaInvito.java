package it.unicam.dmr.doit.invito;

/**
 * Enumerazione che rappresenta i possibili tipi di invito:
 * <ul>
 * <li>PROPOSTA: indica la proposta di partecipazione ad un progetto da parte di un proponente ad un progettista</li>
 * <li>RICHIESTA: indica la richiesta di partecipazione ad un progetto da parte di un progettista ad un proponente</li>
 * <li>VALUTAZIONE: indica la richiesta di valutazione di un progetto da parte di un proponente ad un esperto</li>
 * </ul>
 * 
 * @author Damiano Cacchiarelli
 * @author Matteo Romagnoli
 * @author Roberto Cesetti
 */
public enum TipologiaInvito {
	PROPOSTA,
	RICHIESTA,
	VALUTAZIONE
}
